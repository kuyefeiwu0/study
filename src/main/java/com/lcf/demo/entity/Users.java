package com.lcf.demo.entity;

public class Users {
	private String userCode;
	private String userChar;
	private String userRoles;
	private String userName;
	private String openDate;
	private long openBrh;
	private long prevSn;
	private String nodeId;

	public String getUserCode() {
		return userCode;
	}

	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}

	public String getUserChar() {
		return userChar;
	}

	public void setUserChar(String userChar) {
		this.userChar = userChar;
	}

	public String getUserRoles() {
		return userRoles;
	}

	public void setUserRoles(String userRoles) {
		this.userRoles = userRoles;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getOpenDate() {
		return openDate;
	}

	public void setOpenDate(String openDate) {
		this.openDate = openDate;
	}

	public long getOpenBrh() {
		return openBrh;
	}

	public void setOpenBrh(long openBrh) {
		this.openBrh = openBrh;
	}

	public long getPrevSn() {
		return prevSn;
	}

	public void setPrevSn(long prevSn) {
		this.prevSn = prevSn;
	}

	public String getNodeId() {
		return nodeId;
	}

	public void setNodeId(String nodeId) {
		this.nodeId = nodeId;
	}

}
